    public static MeshData generateMesh(int[,,] field, int isolevel, bool invertModel) {

        var totalTimer = System.Diagnostics.Stopwatch.StartNew();

        List<Vector3> meshVerticies = new List<Vector3>();
        List<Triangle> triangles = new List<Triangle>();

        for (int z = 0; z < field.GetLength(2) - 1; z++) {
            for(int y = 0; y < field.GetLength(1) - 1; y++) {
                for(int x = 0; x < field.GetLength(0) - 1; x++) {
                    Cube cube;
                    cube.corners = new Vector3[8];
                    cube.cornerValues = new int[8];

                    cube.corners[0] = new Vector3(x, y, z);
                    cube.cornerValues[0] = field[x, y, z];

                    cube.corners[1] = new Vector3(x + 1, y, z);
                    cube.cornerValues[1] = field[x + 1, y, z];

                    cube.corners[2] = new Vector3(x + 1, y + 1, z);
                    cube.cornerValues[2] = field[x + 1, y + 1, z];

                    cube.corners[3] = new Vector3(x, y + 1, z);
                    cube.cornerValues[3] = field[x, y + 1, z];

                    cube.corners[4] = new Vector3(x, y, z + 1);
                    cube.cornerValues[4] = field[x, y, z + 1];

                    cube.corners[5] = new Vector3(x + 1, y, z + 1);
                    cube.cornerValues[5] = field[x + 1, y, z + 1];

                    cube.corners[6] = new Vector3(x + 1, y + 1, z + 1);
                    cube.cornerValues[6] = field[x + 1, y + 1, z + 1];

                    cube.corners[7] = new Vector3(x, y + 1, z + 1);
                    cube.cornerValues[7] = field[x, y + 1, z + 1];

                    triangles.AddRange(polygonise(cube, meshVerticies, isolevel, invertModel));

                }
            }
        }

        MeshData meshData = new MeshData();
        meshData.vertices = meshVerticies.ToArray();
        meshData.triangles = triangleListToIntArray(triangles);

        totalTimer.Stop();
        Debug.Log(triangles.Count + " triangles created in " + totalTimer.ElapsedMilliseconds + "ms");

        return meshData;

    }

    private class Worker {

        public Thread thread;
        public bool doneWorking = false;
        public MeshData meshData;

        int[,,] field;
        int isovalue;
        bool inverted;

        public Worker(int[,,] field, int isovalue, bool inverted) {
            this.field = field;
            this.isovalue = isovalue;
            this.inverted = inverted;
            thread = new Thread(doWork);
        }

        public void start() {
            thread.Start();
        }

        public void doWork() {
            meshData = MeshGenerator.generateMesh(field, isovalue, inverted);
            doneWorking = true;
        }

    }

    public static IEnumerator updateMesh(int[,,] field, Mesh mesh, int isovalue, bool inverted) {
        Worker worker = new Worker(field, isovalue, inverted);
        worker.start();
        while (worker.doneWorking == false) {
            yield return null;
        }
        MeshData meshData = worker.meshData; mesh.Clear();
        mesh.vertices = meshData.vertices;
        mesh.triangles = meshData.triangles;
        mesh.RecalculateNormals();
        mesh.RecalculateBounds();
        mesh.RecalculateTangents();
        yield return null;
        
    }

    public static IEnumerator updateMeshWithCollider(int[,,] field, Mesh mesh, MeshCollider collider, int isovalue, bool inverted) {
        Worker worker = new Worker(field, isovalue, inverted);
        worker.start();
        while(worker.doneWorking == false) {
            yield return null;
        }
        MeshData meshData = worker.meshData;
        mesh.Clear();
        mesh.vertices = meshData.vertices;
        mesh.triangles = meshData.triangles;
        mesh.RecalculateNormals();
        mesh.RecalculateBounds();
        mesh.RecalculateTangents();
        collider.sharedMesh = mesh;
        yield return null;

    }

    private static int[] triangleListToIntArray(List<Triangle> triangles) {
        List<int> triangleList = new List<int>();
        foreach(Triangle t in triangles) {
            triangleList.Add(t.vertexIndices[0]);
            triangleList.Add(t.vertexIndices[1]);
            triangleList.Add(t.vertexIndices[2]);
        }

        return triangleList.ToArray();
    }

    /*
       Given a grid cell and an isolevel, calculate the triangular
       facets required to represent the isosurface through the cell.
       Return the number of triangular facets, the array "triangles"
       will be loaded up with the vertices at most 5 triangular facets.
	    0 will be returned if the grid cell is either totally above
       of totally below the isolevel.
    */
    private static List<Triangle> polygonise(Cube cube, List<Vector3> vertices, int isolevel, bool invertModel) {

        List<Triangle> triangles = new List<Triangle>();
        
        /*
            Determine the index into the edge table which
            tells us which vertices are inside of the surface
        */
        int cubeindex = 0;
        if (cube.cornerValues[0] > isolevel) cubeindex |= 1;
        if (cube.cornerValues[1] > isolevel) cubeindex |= 2;
        if (cube.cornerValues[2] > isolevel) cubeindex |= 4;
        if (cube.cornerValues[3] > isolevel) cubeindex |= 8;
        if (cube.cornerValues[4] > isolevel) cubeindex |= 16;
        if (cube.cornerValues[5] > isolevel) cubeindex |= 32;
        if (cube.cornerValues[6] > isolevel) cubeindex |= 64;
        if (cube.cornerValues[7] > isolevel) cubeindex |= 128;

        /* Cube is entirely in/out of the surface */
        if (edgeTable[cubeindex] == 0) return triangles;

        /* Find the vertices where the surface intersects the cube */
        Vector3[] vertlist = new Vector3[12];
        
        if ((edgeTable[cubeindex] & 1) != 0)    vertlist[0] =  linearInterp(cube.corners[0], cube.cornerValues[0], cube.corners[1], cube.cornerValues[1]);
        if ((edgeTable[cubeindex] & 2) != 0)    vertlist[1] =  linearInterp(cube.corners[1], cube.cornerValues[1], cube.corners[2], cube.cornerValues[2]);
        if ((edgeTable[cubeindex] & 4) != 0)    vertlist[2] =  linearInterp(cube.corners[2], cube.cornerValues[2], cube.corners[3], cube.cornerValues[3]);
        if ((edgeTable[cubeindex] & 8) != 0)    vertlist[3] =  linearInterp(cube.corners[3], cube.cornerValues[3], cube.corners[0], cube.cornerValues[0]);
        if ((edgeTable[cubeindex] & 16) != 0)   vertlist[4] =  linearInterp(cube.corners[4], cube.cornerValues[4], cube.corners[5], cube.cornerValues[5]);
        if ((edgeTable[cubeindex] & 32) != 0)   vertlist[5] =  linearInterp(cube.corners[5], cube.cornerValues[5], cube.corners[6], cube.cornerValues[6]);
        if ((edgeTable[cubeindex] & 64) != 0)   vertlist[6] =  linearInterp(cube.corners[6], cube.cornerValues[6], cube.corners[7], cube.cornerValues[7]);
        if ((edgeTable[cubeindex] & 128) != 0)  vertlist[7] =  linearInterp(cube.corners[7], cube.cornerValues[7], cube.corners[4], cube.cornerValues[4]);
        if ((edgeTable[cubeindex] & 256) != 0)  vertlist[8] =  linearInterp(cube.corners[0], cube.cornerValues[0], cube.corners[4], cube.cornerValues[4]);
        if ((edgeTable[cubeindex] & 512) != 0)  vertlist[9] =  linearInterp(cube.corners[1], cube.cornerValues[1], cube.corners[5], cube.cornerValues[5]);
        if ((edgeTable[cubeindex] & 1024) != 0) vertlist[10] = linearInterp(cube.corners[2], cube.cornerValues[2], cube.corners[6], cube.cornerValues[6]);
        if ((edgeTable[cubeindex] & 2048) != 0) vertlist[11] = linearInterp(cube.corners[3], cube.cornerValues[3], cube.corners[7], cube.cornerValues[7]);
        
        /* Create the triangle */
        for (int i = 0; triTable[cubeindex, i] != -1; i += 3) {

            if(!invertModel) {
                Triangle triangle;

                triangle.vertexIndices = new int[3];
                triangle.vertexIndices[0] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i]]);
                triangle.vertexIndices[1] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i + 1]]);
                triangle.vertexIndices[2] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i + 2]]);

                triangles.Add(triangle);
            }
            else {
                Triangle reverse;

                reverse.vertexIndices = new int[3];
                reverse.vertexIndices[2] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i]]);
                reverse.vertexIndices[1] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i + 1]]);
                reverse.vertexIndices[0] = getVertexIndex(vertices, vertlist[triTable[cubeindex, i + 2]]);

                triangles.Add(reverse);
            }
        }

        // Returns the triangles created
        return triangles;

    }

    // returns the index of the vertex and adds a new vertex if necessary
    private static int getVertexIndex(List<Vector3> vertices, Vector3 vertex) {

        for (int i = 0; i < vertices.Count; i++) {
            if (vertex == vertices[i]) return i;
        }
        vertices.Add(vertex);

        return vertices.Count - 1;
    }


    /*
     * p1, p2 = position 1, position 2
     * v1, v2 = value of position 1, value of position 2
     * 
     * formula --> (v2 / (v1 + v2))(p2 - p1) + p1 = final position
     * 
    */
    private static Vector3 linearInterp(Vector3 p1, int v1, Vector3 p2, int v2) {

        Vector3 result = new Vector3();

        float ratio = ((float) v2) / (v1 + v2);

        result.x = ratio * (p2.x - p1.x) + p1.x;
        result.y = ratio * (p2.y - p1.y) + p1.y;
        result.z = ratio * (p2.z - p1.z) + p1.z;

        return result;
    }

